//? Основные размеры данных
//? Для больших объемов данных используются кратные байту единицы:

//? Килобайт (KB) = 1024 байт.
//? Часто используется для измерения небольших файлов, например, текстовых документов.


//? Мегабайт (MB) = 1024 килобайт = 1 048 576 байт.
//? Применяется для файлов среднего размера, таких как изображения или небольшие программы.


//? Гигабайт (GB) = 1024 мегабайт = 1 073 741 824 байт.
//? Часто используется для измерения памяти или размера больших файлов (видео, программы и игры).


//? Терабайт (TB) = 1024 гигабайт = 1 099 511 627 776 байт.
//? Применяется для измерения очень больших объемов данных, например, жестких дисков или облачных хранилищ.


//? Петабайт (PB) = 1024 терабайт.
//? Используется для огромных баз данных и серверов.


//? Эксабайт (EB) = 1024 петабайт.
